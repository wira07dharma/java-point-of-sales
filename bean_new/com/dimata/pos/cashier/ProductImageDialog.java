    public void setImagePath(String path){
        productImageLabel.setIcon(null);
        try{
            try{
                productImageLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/"+path)));
            }
            catch(Exception e){
            }
            if(productImageLabel.getIcon()==null){
                productImageLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/not_available.gif")));
            }
        }
        catch(Exception e){
            System.out.println("err on set ImagePath = "+e.toString());
        }
        pack();
    }
    
